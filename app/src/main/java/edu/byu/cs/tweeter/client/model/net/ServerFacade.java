    /**
     * Returns the feed of the current user. Uses information in
     * the request object to limit the number of statuses returned and to return the next set of
     * statuses after any that were returned in a previous request.
     *
     * @param request contains information about the user whose followees' statuses are to be returned and any
     *                other information required to satisfy the request.
     * @return the statuses.
     */
